package com.h2kinfosys.learn01.day01;

public class ComparisionOperators {

	public static void main(String[] args) {
		int firstData = 100;
		int secondData = 20;
		// < <= > >= != ==  - Output is always boolean
		
		System.out.println("Less Than  " + (firstData < secondData));
		System.out.println("Less Than or equals " + (firstData <= secondData));
		System.out.println("Greater Than  " + (firstData > secondData));
		System.out.println("Greater Than or equals  " + (firstData >= secondData));
		System.out.println("equals  " + (firstData == secondData));
		System.out.println("not equals  " + (firstData != secondData));
		
		

	}

}
